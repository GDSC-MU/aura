  inject: ["theme"],
  components: {
    
  },
  data() {
    return {
      blogsData: [],
      loader: true,
      blogURL: ""
    };
  },
  computed: {
    ...mapState(["config"])
  },
  mounted() {
    this.getMediumBlogs();
  },
  methods: {
    getMediumBlogs() {
      this.loader = true;
      service
        .getAllMediumBlogs(this.config.generalConfig.blogs.medium)
        .then(res => {
          if (res.success) {
            this.loader = false;
            this.blogsData = res.data;
            this.blogURL = this.blogsData.feed.link;
          } else {
            this.loader = false;
          }
        })
        .catch(e => {
          this.loader = false;
        });
    }
  }
};
